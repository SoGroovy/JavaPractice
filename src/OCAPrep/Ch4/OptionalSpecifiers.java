public class OptionalSpecifiers {
    /*
        Types of Optional Specifiers:
        1) static - used for class methods
        2) abstract - used when not providing a method body
        3) synchronized - on OCP, not OCA
        4) native - used when interacting with code from another language IE C++
        5) strictfp - used for making floating-point calculations portable

        **FOCUS ON SYNTAX -------- DOES IT COMPILE?**

        public void walk1() {}
        public final void walk2() {}
        public static final void walk3() {}
        public final static void walk4() {}
        public modifier void walk5() {} (DOES NOT COMPILE)
        public void final walk6() {} (DOES NOT COMPILE)
        final public void walk7() {}
     */
}
