package OCAPrep.Ch3.CreatingAndManipulatingStrings;

public class Concatenation {
    public  void methodA() {
        System.out.println("A" + "B" + 3);              //AB3
        System.out.println(1 + 2 + "c");                //3c
        System.out.println(5 + 7 + "c" + false);        //12cfalse
    }
}
